How to Choose a Good Code Word

A strong family code word has three properties: it is unusual, it is specific, and it is not findable online.

Unusual means it is not a common word or phrase. "Apple" or "hello" are too generic. "Password" is terrible. You want something distinctive enough that no one would guess it by accident.

Specific means it refers to something meaningful to your family — an inside joke, a favorite memory, a made-up word, or a combination of things only your family would put together. "Blue bucket at the cabin" or "Grandma''s purple fence" or a nonsense word you invented together all work well.

