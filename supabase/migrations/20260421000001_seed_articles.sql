What Is a Family Code Word?

A family code word (sometimes called a family safe word or verification word) is a secret phrase that only your immediate family members know. When anyone in the family calls with an emergency — especially one involving money — they must say the code word first. If the caller cannot produce the code word, the call is treated as a scam and ended immediately.

